package cnuphys.bCNU.drawable;

/**
 * For monitoring changes to a DrawableSet.Enumerates the types of changes that
 * can occur to a drawable.
 * 
 * @author heddle
 * 
 */
public enum DrawableChangeType {
	ADDED, DESELECTED, DOUBLECLICKED, HIDDEN, MODIFIED, MOVED, REMOVED, RESIZED, ROTATED, SELECTED, SHOWN, LISTCLEARED, LISTHIDDEN, LISTSHOWN;
}
