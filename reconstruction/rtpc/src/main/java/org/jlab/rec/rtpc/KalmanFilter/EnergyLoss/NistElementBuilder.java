    public double GetAtomicMass(int Z, int N) {
        double mass = 0.0;
        if (Z > 0 && Z < maxNumElements) {
            int i = N - nFirstIsotope[Z];
            if (i >= 0 && i < nIsotopes[Z]) {
                mass = massIsotopes[i + idxIsotopes[Z]] +
                        Z * electron_mass_c2 - bindingEnergy[Z];
            }
        }
        return mass;
    }

    public double GetIsotopeAbundance(int Z, int N) {
        double x = 0.0;
        if (Z > 0 && Z < maxNumElements) {
            int i = N - nFirstIsotope[Z];
            if (i >= 0 && i < nIsotopes[Z]) {
                x = relAbundance[i + idxIsotopes[Z]];
            }
        }
        return x;
    }


    public int GetNistFirstIsotopeN(int Z) {
        return (Z > 0 && Z < maxNumElements) ? nFirstIsotope[Z] : 0;
    }


    public int GetNumberOfNistIsotopes(int Z) {
        return (Z > 0 && Z < maxNumElements) ? nIsotopes[Z] : 0;
    }


    public Vector<String> GetElementNames() {
        return elmNames;
    }


    public Element FindElement(int Z) {
        Vector<Element> theElementTable = Element.GetElementTable();
        return (Z > 0 && Z < maxNumElements && elmIndex[Z] >= 0) ?
                (theElementTable).get(elmIndex[Z]) : null;
    }

    public double GetAtomicMassAmu(int Z) {
        return (Z > 0 && Z < maxNumElements) ? atomicMass[Z] : 0.0;
    }

    public int GetZ(String name) {
        int Z = maxNumElements;
        do {
            --Z;
        } while (Z > 0 && elmSymbol[Z] != name);
        return Z;
    }

    public Element FindOrBuildElement(int Z) {
        Element anElement = FindElement(Z);
        if (anElement == null && Z > 0 && Z < maxNumElements) {
            if (elmIndex[Z] < 0) {
                anElement = BuildElement(Z);
            }
            if (anElement != null) {
                elmIndex[Z] = (int) (anElement.GetIndex());
            }
        }
        return anElement;
    }

    private void AddElement(String name, int Z, int nc,
                            int[] N, double[] A,
                            double[] sA, double[] W) {

        if (Z >= maxNumElements) return;

        if (index + nc >= maxAbundance) return;

        elmSymbol[Z] = name;
        atomicMass[Z] = 0.0;
        idxIsotopes[Z] = index;
        nIsotopes[Z] = nc;

        nFirstIsotope[Z] = N[0];
        double ww = 0.0;
        double www;

        for (int i = 0; i < nc; ++i) {
            www = 0.01 * W[i];
            massIsotopes[index] = A[i] * amu_c2 - Z * electron_mass_c2 + bindingEnergy[Z];
            relAbundance[index] = www;

            atomicMass[Z] += www * (A)[i];
            ww += www;
            ++index;
        }

        if (ww != 1.0) {
            int idx = idxIsotopes[Z];
            atomicMass[Z] /= ww;
            for (int j = 0; j < nc; ++j) {
                relAbundance[idx + j] /= ww;
            }
        }

    }

    private Element BuildElement(int Z) {
        Element theElement = null;
        if (Z < 1 || Z >= maxNumElements) {
            return theElement;
        }
        double Aeff = atomicMass[Z];

        int nc = nIsotopes[Z];
        int n0 = nFirstIsotope[Z];
        int idx = idxIsotopes[Z];
        Vector<Isotope> iso = new Vector<>();
        Isotope ist;
        for (int i = 0; i < nc; ++i) {
            if (relAbundance[idx + i] > 0.0) {
                ist = new Isotope(elmSymbol[Z], Z, n0 + i, GetAtomicMass(Z, n0 + i) * g / (mole * amu_c2), 0);
                iso.add(ist);
            }
        }
        int ni = iso.size();
        double w;
        theElement = new Element(elmSymbol[Z], elmSymbol[Z], ni);
        for (int j = 0; j < ni; ++j) {
            w = relAbundance[idx + (iso.get(j)).GetN() - n0];
            ist = iso.get(j);
            theElement.AddIsotope(ist, w);
        }
        theElement.SetNaturalAbundanceFlag(true);
        return theElement;
    }


